package chap13.section01.example13;

public class S13_Math2 {
public static void main(String[] args) {
	double a=5.3;
	
	
//	TODO: Math.round(변수): 반올림(1쨰자리만) 결과:정수
	System.out.println(Math.round(a));
	
	
//	TODO: Math.floor(변수): 내림(1쨰자리만)	결과:실수
	System.out.println(Math.floor(a)); 
	
	
//	TODO: Math.ceil(변수): 올림(1쨰자리만) 결과: 실수
	System.out.println(Math.ceil(a));
}
}
