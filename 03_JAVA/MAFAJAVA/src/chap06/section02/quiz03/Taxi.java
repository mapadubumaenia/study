package chap06.section02.quiz03;

public class Taxi {
  String model;
  int maxspeed;
  
  
public Taxi() {
	super();
}


public Taxi(String model) {
	super();
	this.model = model;
}


public Taxi(int maxspeed) {
	super();
	this.maxspeed = maxspeed;
}


public Taxi(String model, int maxspeed) {
	super();
	this.model = model;
	this.maxspeed = maxspeed;
}
  


  
}
